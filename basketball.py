class Player: 
    def __init__(self, player_b):
        self.name = player_b["name"]
        self.age = player_b["age"]
        self.position = player_b["position"]
        self.team = player_b["team"]


    def __repr__(self):
        return "Player: {}, Age: {}, Position: {}, Team: {}".format(self.name, self.age, self.position, self.team)

